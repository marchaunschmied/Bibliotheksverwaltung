package model;

public class Regal {
	private int id;
	private int standort_id;
	private int medium_id;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getStandort_id() {
		return standort_id;
	}
	public void setStandort_id(int standort_id) {
		this.standort_id = standort_id;
	}
	public int getMedium_id() {
		return medium_id;
	}
	public void setMedium_id(int medium_id) {
		this.medium_id = medium_id;
	}
}
